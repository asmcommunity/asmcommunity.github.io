message	MACRO	YES,NO
	LOCAL	DOYES,DONO,DOEXIT
	IFNB	<NO>
	JC	DONO
	ENDIF
	IFNB	<YES>
	JC	DOEXIT
	CALL	YES
	JMP	DOEXIT
	ENDIF
	IFNB	<NO>
DONO:
	CALL	NO
	ENDIF
DOEXIT:
	ENDM
incword	MACRO	WORD
