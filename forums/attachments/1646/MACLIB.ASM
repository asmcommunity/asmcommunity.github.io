init	MACRO	START,COUNT,VALUE
	LOCAL	LABEL
	PUSH	SI
	PUSH	CX
	PUSH	AX
	MOV	SI,OFFSET START
	MOV	CX,COUNT
	MOV	AL,VALUE
LABEL:
	MOV	[SI],AL
	INC	SI
	LOOP	LABEL
	POP	AX
	POP	CX
	POP	SI
	ENDM
